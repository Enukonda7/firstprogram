package first1;
import java.util.Scanner;



public class Even_or_odd {
	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		System.out.println("enter a number");
		int x=sc.nextInt();
		if(x%2==0) { 
			System.out.println("even");
		}
		
		else {
			System.out.println("odd");
		}
		
	
		
	}

}
